#include<iostream>
using namespace std;
int main()
{
    int a=5;
    int b=10;
    int sum;
    sum=a+b;
    cout<<"The sum of"<<a <<"and"<< b <<"is"<< sum <<"\n";
    int diff;
    diff=a-b;
    cout<<"The diff of"<<a <<"and"<< b <<"is"<< diff <<"\n";
    int multiply;
    multiply=a*b;
    cout<<"The multiplication of"<<a <<"and"<< b <<"is"<< multiply <<"\n";
    int div; 
    div=a/b;
    cout<<"The division of"<<a <<"and"<< b <<"is"<< div <<"\n";
    return 0;
}